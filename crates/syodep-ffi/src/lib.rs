//! C ABI for the syodep core, consumed by the Qt shell.
//!
//! Conventions:
//!
//! - Every function is `extern "C"` and panic-safe (panics are caught and
//!   reported as errors / no-ops rather than unwinding into C++).
//! - The shell owns one `SyoApp` per window, created with [`syo_app_new`]
//!   and destroyed with [`syo_app_free`].
//! - Strings returned by the core are freed with [`syo_string_free`];
//!   bitmaps with [`syo_bitmap_free`]. Strings passed in are borrowed UTF-8.
//! - Key events arrive as the textual chord syntax from
//!   `syodep_config::keys` (e.g. `j`, `G`, `<C-d>`); the shell performs the
//!   purely mechanical Qt-event-to-string translation.
//!
//! The header `include/syodep_ffi.h` is generated by cbindgen at build time.
//!
//! Safety: this crate is the FFI boundary; all `unsafe` here is confined to
//! pointer/CString handling at the very edge. No MuPDF or Qt types cross
//! this boundary in either direction.

use std::ffi::{c_char, CStr, CString};
use std::panic::{catch_unwind, AssertUnwindSafe};
use std::path::PathBuf;

use syodep_config::keys::parse_sequence;
use syodep_config::Config;
use syodep_core::{App, Effects};
use syodep_storage::Storage;

/// Opaque application handle.
pub struct SyoApp {
    app: App,
    /// Resolved starting directory for the Open dialog (absolute when known).
    open_dir: String,
    /// Human-readable provenance of `open_dir`, reported by `--check`.
    open_dir_source: String,
}

/// Outcome of resolving the Open-dialog starting directory.
struct OpenDirResolution {
    /// Directory the dialog should open in (empty if even the CWD is unknown,
    /// in which case the shell lets Qt pick its own default).
    path: String,
    /// Short note on where the value came from, for `--check`.
    source: String,
    /// A startup warning to surface, set only when a configured directory was
    /// requested but could not be used.
    warning: Option<String>,
}

/// Resolve the Open-dialog starting directory.
///
/// Precedence: a configured `files.open_dir` that exists as a directory wins;
/// otherwise the launch (current working) directory is used. A configured path
/// that is missing or not a directory falls back to the launch directory and
/// produces a warning.
fn resolve_open_dir(configured: Option<&str>) -> OpenDirResolution {
    let cwd = std::env::current_dir()
        .ok()
        .map(|p| p.display().to_string())
        .unwrap_or_default();

    match configured {
        Some(raw) if !raw.trim().is_empty() => {
            if PathBuf::from(raw).is_dir() {
                OpenDirResolution {
                    path: raw.to_owned(),
                    source: format!("config: files.open_dir ({raw})"),
                    warning: None,
                }
            } else {
                OpenDirResolution {
                    path: cwd,
                    source: format!("config dir not found ({raw}) — using launch directory"),
                    warning: Some(format!(
                        "open_dir not found: {raw} — using launch directory"
                    )),
                }
            }
        }
        _ => OpenDirResolution {
            path: cwd,
            source: "launch directory".to_owned(),
            warning: None,
        },
    }
}

/// Effect bits returned by input functions.
pub const SYO_EFFECT_REDRAW: u32 = 1;
pub const SYO_EFFECT_QUIT: u32 = 2;
pub const SYO_EFFECT_OPEN_FILE_DIALOG: u32 = 4;

fn effects_to_bits(effects: Effects) -> u32 {
    let mut bits = 0;
    if effects.redraw {
        bits |= SYO_EFFECT_REDRAW;
    }
    if effects.quit {
        bits |= SYO_EFFECT_QUIT;
    }
    if effects.open_file_dialog {
        bits |= SYO_EFFECT_OPEN_FILE_DIALOG;
    }
    bits
}

/// A page to draw, in canvas pixel coordinates.
#[repr(C)]
#[derive(Clone, Copy)]
pub struct SyoVisiblePage {
    pub page: usize,
    pub x: f32,
    pub y: f32,
    pub width: f32,
    pub height: f32,
}

/// The caret rectangle in canvas pixels, for the overlay the shell paints.
/// `valid` is 0 when not in caret focus mode or no caret is placed, in which case
/// the remaining fields are unspecified.
#[repr(C)]
#[derive(Clone, Copy)]
pub struct SyoCaret {
    pub valid: u8,
    pub page: usize,
    pub x: f32,
    pub y: f32,
    pub width: f32,
    pub height: f32,
}

/// A tightly packed RGBA8 image (stride = width * 4).
#[repr(C)]
pub struct SyoBitmap {
    pub width: u32,
    pub height: u32,
    pub data: *mut u8,
    pub data_len: usize,
}

unsafe fn cstr_to_path(ptr: *const c_char) -> Option<PathBuf> {
    if ptr.is_null() {
        return None;
    }
    let s = unsafe { CStr::from_ptr(ptr) }.to_str().ok()?;
    Some(PathBuf::from(s))
}

fn to_c_string(s: String) -> *mut c_char {
    // Interior NULs cannot come from our own status/error strings, but be
    // defensive at the boundary.
    CString::new(s.replace('\0', " "))
        .expect("NUL bytes were just stripped")
        .into_raw()
}

/// Create the application core.
///
/// `config_path` and `db_path` may be NULL: a NULL config uses built-in
/// defaults, a NULL database path disables persistence. A config file that
/// fails to parse is reported as a startup warning (see
/// `syo_app_take_warnings`) and replaced by defaults; it never aborts.
///
/// # Safety
/// `config_path` and `db_path` must be NULL or valid NUL-terminated strings.
#[no_mangle]
pub unsafe extern "C" fn syo_app_new(
    config_path: *const c_char,
    db_path: *const c_char,
) -> *mut SyoApp {
    let config_path = unsafe { cstr_to_path(config_path) };
    let db_path = unsafe { cstr_to_path(db_path) };
    let result = catch_unwind(|| {
        let mut warnings = Vec::new();
        let config = match &config_path {
            Some(path) => match Config::load(path) {
                Ok(config) => config,
                Err(e) => {
                    warnings.push(format!("using default config: {e}"));
                    Config::default()
                }
            },
            None => Config::default(),
        };
        let storage = match &db_path {
            Some(path) => match Storage::open(path) {
                Ok(storage) => Some(storage),
                Err(e) => {
                    warnings.push(format!("persistence disabled: {e}"));
                    None
                }
            },
            None => None,
        };
        let open = resolve_open_dir(config.files.open_dir.as_deref());
        if let Some(warning) = open.warning {
            warnings.push(warning);
        }
        let mut app = App::new(config, storage);
        for warning in warnings {
            app.report_error(warning);
        }
        SyoApp {
            app,
            open_dir: open.path,
            open_dir_source: open.source,
        }
    });
    match result {
        Ok(app) => Box::into_raw(Box::new(app)),
        Err(_) => std::ptr::null_mut(),
    }
}

/// Destroy the application core, persisting the reading position.
///
/// # Safety
/// `app` must be a pointer returned by `syo_app_new`, not freed before.
#[no_mangle]
pub unsafe extern "C" fn syo_app_free(app: *mut SyoApp) {
    if !app.is_null() {
        let boxed = unsafe { Box::from_raw(app) };
        let _ = catch_unwind(AssertUnwindSafe(move || drop(boxed)));
    }
}

/// Open a document. Returns true on success; on failure the previous
/// document (if any) stays open and the error is shown in the status text.
///
/// # Safety
/// `app` must be valid; `path` must be a valid NUL-terminated string.
#[no_mangle]
pub unsafe extern "C" fn syo_app_open_document(app: *mut SyoApp, path: *const c_char) -> bool {
    let Some(app) = (unsafe { app.as_mut() }) else {
        return false;
    };
    let Some(path) = (unsafe { cstr_to_path(path) }) else {
        return false;
    };
    catch_unwind(AssertUnwindSafe(|| match app.app.open_document(&path) {
        Ok(()) => true,
        Err(e) => {
            app.app.report_error(e.to_string());
            false
        }
    }))
    .unwrap_or(false)
}

/// True when a document is open.
///
/// # Safety
/// `app` must be valid.
#[no_mangle]
pub unsafe extern "C" fn syo_app_has_document(app: *const SyoApp) -> bool {
    unsafe { app.as_ref() }
        .map(|a| a.app.has_document())
        .unwrap_or(false)
}

/// Inform the core of the canvas size in physical pixels.
///
/// # Safety
/// `app` must be valid.
#[no_mangle]
pub unsafe extern "C" fn syo_app_set_viewport(app: *mut SyoApp, width: f32, height: f32) {
    if let Some(app) = unsafe { app.as_mut() } {
        let _ = catch_unwind(AssertUnwindSafe(|| {
            app.app.set_viewport_size(width, height)
        }));
    }
}

/// Feed one key chord (textual syntax, e.g. "j", "G", "<C-d>", "<Esc>").
/// Returns SYO_EFFECT_* bits.
///
/// # Safety
/// `app` must be valid; `key` must be a valid NUL-terminated string.
#[no_mangle]
pub unsafe extern "C" fn syo_app_key_event(app: *mut SyoApp, key: *const c_char) -> u32 {
    let Some(app) = (unsafe { app.as_mut() }) else {
        return 0;
    };
    if key.is_null() {
        return 0;
    }
    let Ok(key) = (unsafe { CStr::from_ptr(key) }).to_str() else {
        return 0;
    };
    catch_unwind(AssertUnwindSafe(|| {
        match parse_sequence(key).as_deref() {
            // The shell sends exactly one chord per key event.
            Ok([chord]) => effects_to_bits(app.app.handle_key(*chord)),
            _ => 0,
        }
    }))
    .unwrap_or(0)
}

/// Scroll by a pixel delta (mouse wheel). Returns SYO_EFFECT_* bits.
///
/// # Safety
/// `app` must be valid.
#[no_mangle]
pub unsafe extern "C" fn syo_app_scroll_by(app: *mut SyoApp, dx: f32, dy: f32) -> u32 {
    let Some(app) = (unsafe { app.as_mut() }) else {
        return 0;
    };
    catch_unwind(AssertUnwindSafe(|| {
        effects_to_bits(app.app.scroll_by_px(dx, dy))
    }))
    .unwrap_or(0)
}

/// Fill `out` with up to `capacity` visible pages; returns the number of
/// visible pages (which may exceed `capacity`; call again with more room).
///
/// # Safety
/// `app` must be valid; `out` must point to at least `capacity` elements.
#[no_mangle]
pub unsafe extern "C" fn syo_app_visible_pages(
    app: *const SyoApp,
    out: *mut SyoVisiblePage,
    capacity: usize,
) -> usize {
    let Some(app) = (unsafe { app.as_ref() }) else {
        return 0;
    };
    let Ok(pages) = catch_unwind(AssertUnwindSafe(|| app.app.visible_pages())) else {
        return 0;
    };
    if !out.is_null() {
        for (i, page) in pages.iter().take(capacity).enumerate() {
            unsafe {
                *out.add(i) = SyoVisiblePage {
                    page: page.page,
                    x: page.rect.x,
                    y: page.rect.y,
                    width: page.rect.width,
                    height: page.rect.height,
                };
            }
        }
    }
    pages.len()
}

/// The caret rectangle (canvas pixels) for the overlay. When `valid` is 0 the
/// shell draws nothing.
///
/// # Safety
/// `app` must be valid.
#[no_mangle]
pub unsafe extern "C" fn syo_app_caret(app: *const SyoApp) -> SyoCaret {
    let invalid = SyoCaret {
        valid: 0,
        page: 0,
        x: 0.0,
        y: 0.0,
        width: 0.0,
        height: 0.0,
    };
    let Some(app) = (unsafe { app.as_ref() }) else {
        return invalid;
    };
    catch_unwind(AssertUnwindSafe(|| match app.app.caret_screen_rect() {
        Some((page, rect)) => SyoCaret {
            valid: 1,
            page,
            x: rect.x,
            y: rect.y,
            width: rect.width,
            height: rect.height,
        },
        None => invalid,
    }))
    .unwrap_or(invalid)
}

