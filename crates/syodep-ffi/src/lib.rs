/// Render a page at the current zoom. Returns NULL on failure. The result
/// must be freed with `syo_bitmap_free`.
///
/// # Safety
/// `app` must be valid.
#[no_mangle]
pub unsafe extern "C" fn syo_app_render_page(app: *mut SyoApp, page: usize) -> *mut SyoBitmap {
    let Some(app) = (unsafe { app.as_mut() }) else {
        return std::ptr::null_mut();
    };
    if !app.app.has_document() {
        return std::ptr::null_mut();
    }
    let result = catch_unwind(AssertUnwindSafe(|| match app.app.render_page(page) {
        Ok(bitmap) => {
            let mut data = bitmap.data.clone().into_boxed_slice();
            let out = SyoBitmap {
                width: bitmap.width,
                height: bitmap.height,
                data: data.as_mut_ptr(),
                data_len: data.len(),
            };
            std::mem::forget(data);
            Box::into_raw(Box::new(out))
        }
        Err(e) => {
            app.app.report_error(e.to_string());
            std::ptr::null_mut()
        }
    }));
    result.unwrap_or(std::ptr::null_mut())
}

/// Free a bitmap returned by `syo_app_render_page`.
///
/// # Safety
/// `bitmap` must be NULL or a pointer returned by `syo_app_render_page`,
/// not freed before.
#[no_mangle]
pub unsafe extern "C" fn syo_bitmap_free(bitmap: *mut SyoBitmap) {
    if bitmap.is_null() {
        return;
    }
    unsafe {
        let bitmap = Box::from_raw(bitmap);
        if !bitmap.data.is_null() {
            drop(Box::from_raw(std::ptr::slice_from_raw_parts_mut(
                bitmap.data,
                bitmap.data_len,
            )));
        }
    }
}

/// Status line text. Free with `syo_string_free`.
///
/// # Safety
